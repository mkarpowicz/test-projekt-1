<h3>Regulamin Koła Naukowego Instytutu Rusycystyki "Warsztat Literaturoznawczy"</h3>
<br>
<h4>Nazwa i siedziba Organizacji</h4>
<br>
<h5>&sect; 1</h5>
<ul>
  <li>Koło Naukowe Instytutu Rusycystyki UW "Warsztat Literaturoznawczy", zwane dalej Kołem, działa na podstawie ustawy – Prawo o szkolnictwie wyższym, Statutu UW oraz niniejszego Regulaminu.
  <li>Siedzibą Koła jest Instytut Rusycystyki UW.
</ul>
<h4>Cele Koła i sposoby ich realizacji</h4>
<br>
<h5>&sect; 2</h5>
Celem Koła jest:
<ul>
  <li>działalność popularyzatorska mająca na celu krzewienie wiedzy w dyscyplinie reprezentowanej przez Koło,
  <li>umożliwienie wymiany doświadczeń i prezentowania wyników własnych prac badawczych przez studentów i młodych absolwentów,
  <li>rozwijanie osobowości członków Koła.
</ul>
<h5>&sect; 3</h5>
Koło realizuje swoje cele poprzez:
<ul>
  <li>organizowanie odczytów wygłaszanych przez członków Koła lub zaproszonych gości: specjalistów z dziedziny literaturoznawstwa i pisarzy,
  <li>podejmowanie i prowadzenie przez członków Koła zespołowych i indywidualnych prac naukowych,
  <li>organizowanie i udział w seminariach, konferencjach, sympozjach i zjazdach,
  <li>współpracę z innymi kołami naukowymi, instytutami naukowymi i szkołami średnimi w kraju i za granicą,
  <li>publikowanie własnych osiągnięć naukowych w kraju i za granicą.
</ul>
<h4>Członkostwo</h4>
<br>
<h5>&sect; 4</h5>
Członkiem Koła może być każdy student Uniwersytetu Warszawskiego. O członkowstwo Koła może ubiegać się również doktorant UW, który uzasadni zainteresowanie realizacją jego celów.<br>
<br>
<h5>&sect; 5</h5>
<ul>
  <li>Nabycie członkostwa Koła następuje wskutek uchwały Zarządu Organizacji akceptującej pisemną deklarację członkowską.
  <li>Utrata członkowska następuje: na skutek uchwały Zarządu Koła; na skutek oświadczenia woli wystąpienia z Koła, podanego do wiadomości Zarządu; z powodu zgonu członka; z powodu skreślenia z listy studentów.
  <li>Od decyzji Zarządu o wykluczeniu z Koła członek może się odwołać do Walnego Zgromadzenia Członków.
</ul>
<h5>&sect; 6</h5>
Członek Koła ma prawo do:
<ul>
  <li>czynnego i biernego prawa wyborczego do władz Koła,
  <li>udziału w pracach Koła, korzystania ze wszystkich urządzeń, obiektów i stwarzanych przez Koło możliwości,
  <li>używania odznak i znaków Koła, a także jego reprezentowania na wszelkich imprezach i spotkaniach o charakterze nie kolidującym z celami i filozofią działania Koła,
  <li>zgłaszania opinii, wniosków i postulatów pod adresem władz Koła.
</ul>
<h5>&sect; 7</h5>
Do obowiązków członków należy:
<ul>
  <li>aktywne uczestniczenie w pracach i realizacji celów Koła,
  <li>stosowanie się w swojej działalności do uchwał oraz wytycznych władz Koła,
  <li>regularne opłacanie składek członkowskich na rzecz Koła.
</ul>
<h4>Władze Koła: powoływanie, odwoływanie, kompetencje</h4>
<br>
<h5>&sect; 8</h5>
Do władz Koła należy:
<ul>
  <li>Walne Zgromadzenie Członków,
  <li>Zarząd.
</ul>
<h5>&sect; 9</h5>
Do kompetencji Walnego Zgromadzenia Członków należy:
<ul>
  <li>uchwalanie i zmiany Regulaminu Koła,
  <li>wybór Prezesa, a na jego wniosek innych członków Zarządu,
  <li>rozpatrywanie sprawozdań z działalności Zarządu,
  <li>podejmowanie decyzji o rozwiązaniu Koła,
  <li>rozpatrywanie innych spraw wniesionych przez członków Koła.
</ul>
<h5>&sect; 10</h5>
<ul>
  <li>Walne Zgromadzenie Członków zwoływane jest przez prezesa Zarządu Koła co najmniej raz w roku oraz na żądanie co najmniej połowy członków Koła.
  <li>W Walnym Zgromadzeniu Członków uczestniczą wszyscy członkowie Koła.
</ul>
<h5>&sect; 11</h5>
Walne Zgromadzenie Członków podejmuje decyzje większością bezwzględną głosów w obecności co najmniej połowy uprawnionych.<br>
<br>
<h5>&sect; 12</h5>
Zarząd Koła składa się z prezesa, wiceprezesa, sekretarza, skarbnika oraz innych członków jeżeli Walne Zgromadzenie Członków tak postanowi.<br>
<br>
<h5>&sect; 13</h5>
Kadencja Zarządu trwa jeden rok.<br>
<br>
<h5>&sect; 14</h5>
Zarząd podejmuje decyzje większością głosów swoich członków we wszystkich sprawach nie zastrzeżonych do kompetencji Walnego Zgromadzenia Członków.<br>
<br>
<h5>&sect; 15</h5>
<ul>
  <li>Prezes Zarządu kieruje pracami Zarządu oraz reprezentuje Koło na zewnątrz.
  <li>Prezes Zarządu w terminie do dnia 31 stycznia każdego roku składa Rektorowi coroczne sprawozdanie z działalności Koła za rok poprzedni. 
  <li>Prezes Koła zobowiązany jest przedkładać Rektorowi wszelkie zmiany w Regulaminie Koła, zmiany we władzach Koła, i aktualny wykaz członków Koła.
</ul>

