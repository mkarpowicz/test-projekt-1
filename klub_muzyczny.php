Koło naukowe "Warsztat Literaturoznawczy" pragnie zaprosić na spotkania Klubu Muzycznego Instytutu Rusycystyki UW.<br>
<br>
Klub Muzyczny powstał dzięki inicjatywie Marty Tomasik (studentka IV roku rusycystyki) i Wiktorii Milewskiej (studentka V roku rusycystyki). Obecnie do Klubu należą studenci rusycystyki, studenci innych kierunków UW, a nawet Politechniki.<br>
<br>
<b>W repertuarze:</b>
<ul>
  <li>Белые розы,</li>
  <li>Катюша,</li>
  <li>Конь,</li>
  <li>За туманами,</li>
  <li>i wiele innych przebojów...</li>
</ul>
Celem działalności Klubu jest przygotowanie koncertu rosyjskich piosenek na Wydziale Lingwistyki Stosowanej (ul. Szturmowa) wiosną 2010 oraz happeningu przed akademikiem na ul. Smyczkowej latem 2010.<br>
<br>
Spotkania odbywają się w każdą środę w godzinach 19:00-21:00 w akademiku UW przy ul. Smyczkowej 5/7, w sali "G6".<br>
<br>
Klub Muzyczny zaprasza wszystkich chętnych do wspólnego śpiewania!<br>
